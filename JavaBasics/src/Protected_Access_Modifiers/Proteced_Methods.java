package Protected_Access_Modifiers;

public class Proteced_Methods {
	
		private int a;
		protected int b;
		public int c;
		
		protected void protected_method() {
			
			System.out.println("This is a protected method");
		}
		
		public void public_methods() {
			
			System.out.println("This is a public  method");
		}
}
